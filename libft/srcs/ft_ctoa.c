#include "libft.h"

char	*ft_ctoa(int c)
{
	char	*str;

	if ((str = (char *)malloc(sizeof(char) * 2)) == NULL)
		return (NULL);
	str[0] = c;
	str[1] = '\0';
	return (str);
}
