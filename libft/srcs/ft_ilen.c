#include "libft.h"

size_t	ft_ilen(int n)
{
	int		len;

	len = 0;
	if (n >= 10 || n <= -10)
	{
		n /= 10;
		len++;
	}
	len += (n < 0) ? 1 : 0;
	n *= (n < 0) ? -1 : 1;
	if (!n)
		return (1);
	while (++len && n >= 10)
		n /= 10;
	return (len);
}
